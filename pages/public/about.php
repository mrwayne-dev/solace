      <p>
        At HealthRunCare, privacy is not an afterthought — it’s a core design principle.  
        As a Wales-based healthcare technology provider regulated under <strong>Healthcare Inspectorate Wales (HIW)</strong>, 
        we adhere to the highest standards of data protection, including <strong>GDPR</strong>, <strong>UK Data Protection Act 2018</strong>, 
